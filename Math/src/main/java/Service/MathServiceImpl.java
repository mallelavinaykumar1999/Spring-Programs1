package Service;

public class MathServiceImpl implements MathService{
  public int addition(int a,int b) {
	  return a+b;
  }
  public int subtraction(int a,int b) {
	  return a-b;
  }
  public int multliplication(int a,int b) {
	  return a*b;
  }
  public int division(int a,int b) {
	  return a/b;
  }
}
